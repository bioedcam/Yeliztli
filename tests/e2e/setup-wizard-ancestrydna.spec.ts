/**
 * Step 44 — Setup-wizard E2E for AncestryDNA (ADNA-11; Plan §13.1).
 *
 * Drives the full first-run wizard against an AncestryDNA raw-data upload,
 * parameterised by the installed `vep_bundle.version`:
 *
 *   1. **Happy path (bundle v2.0.0 already installed)** — user walks the
 *      wizard, drops an AncestryDNA `.txt` on the Upload step, sees the
 *      "Sample Uploaded" summary with `ancestrydna_v2.0` as the parsed
 *      `file_format`, clicks "Go to Dashboard" and lands on `/` with the
 *      fresh-install gate cleared (`needs_setup=false`).
 *
 *   2. **Pre-v2.0.0 bundle gate** — same wizard walk, but the first
 *      `POST /api/ingest` returns HTTP 409 with the Plan §5.4
 *      `bundle_version_too_old` payload. The Upload step renders the
 *      in-wizard banner with the installed/required versions and the
 *      manifest `update_url`. Clicking the update CTA fires
 *      `POST /api/updates/trigger` for `vep_bundle`, the mocked job
 *      reports `complete` on the first poll, the banner clears, and a
 *      second `POST /api/ingest` (replayed by the user) succeeds with
 *      202 + `ancestrydna_v2.0`.
 *
 * Every backend endpoint is intercepted with `page.route()` so the spec
 * stays deterministic across Chromium / Firefox / WebKit without touching
 * the real ingest pipeline or the ~600 MB VEP bundle. Patterned after
 * `setup-wizard-lai.spec.ts` (Step 32) and `lai-ancestrydna.spec.ts`
 * (Step 25).
 */

